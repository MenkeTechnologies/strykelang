//! Smoke tests for the 4,600+ Wolfram-namespace builtins.
//!
//! Each Wolfram module under `strykelang/math_wolfram_*.rs` defines
//! ~28 builtin fns; that's ~2,300+ functions previously uncovered by
//! `tests/suite/`. Generating per-fn behavioural tests for every one
//! would be 1) impractical (many take 4–8 domain-specific args with
//! tight numeric tolerances) and 2) noise per CLAUDE.md "no generic
//! tests that waste time". This file instead pins a single critical
//! anti-crash property: **every Wolfram builtin must handle an empty
//! argv without panicking** — either return an error (`StrykeResult::
//! Err(_)`) or a sensible default value. Panics (index-out-of-bounds,
//! `unwrap` on `None`, integer overflow in debug, divide-by-zero,
//! etc.) are bugs and surface here as test failures.
//!
//! Coverage shape: ONE `#[test]` walks every registered builtin name
//! (loaded from `tests/fixtures/wolfram_builtins.txt`) and calls it
//! via `name()` in Stryke. Panics are caught via `std::panic::
//! catch_unwind`; the failure report lists every panicking builtin so
//! one CI run surfaces every offender, not just the first.
//!
//! The names fixture is generated by:
//!   ```
//!   grep -h "^fn builtin_" strykelang/math_wolfram_*.rs \
//!     | sed 's/^fn builtin_//; s/(.*//' | sort -u \
//!     > tests/fixtures/wolfram_builtins.txt
//!   ```
//! Regenerate after adding new Wolfram builtins.

use std::panic::{catch_unwind, AssertUnwindSafe};
use stryke::vm_helper::VMHelper;

use crate::common::GLOBAL_FLAGS_LOCK;

const NAMES_FIXTURE: &str = include_str!("../fixtures/wolfram_builtins.txt");

/// Stryke reserved words that shadow Wolfram builtin names — these
/// would parse as the language construct, not a function call, so
/// `name()` fails at PARSE time (not runtime) and isn't a builtin
/// bug. Skip them in the smoke walk.
const STRYKE_RESERVED: &[&str] = &[
    "if", "elsif", "else", "unless", "while", "until", "for", "foreach", "do", "last", "next",
    "redo", "return", "sub", "my", "var", "val", "our", "local", "state", "use", "no", "package", "require",
    "eval", "die", "warn", "print", "printf", "say", "and", "or", "not", "xor", "cmp", "eq", "ne",
    "lt", "gt", "le", "ge", "qw", "qr", "q", "qq",
];

/// Known-offender allowlist: builtins that PANIC on empty args.
/// The list is meant to SHRINK — each name here is a real bug in the
/// builtin's empty-input handling. Adding a new name without a paired
/// fix in flight is a regression-tag, not a solution.
const PANIC_ALLOWLIST: &[&str] = &[
    // (none — all 28 prior offenders fixed in their math_wolfram_*.rs
    // bodies with `is_empty()` guards / shape checks / underflow
    // avoidance. Re-add a name here only if a new builtin starts
    // panicking and the fix needs follow-up.)
];

/// Known-hanger allowlist: builtins that exceed PER_CALL_TIMEOUT_MS
/// on empty args. Carlson elliptic integrals (`rd`/`rf`/`rj`) iterate
/// to numeric convergence; `qs_relation` runs the quadratic-sieve
/// inner loop. Each one needs an early-return guard for empty input.
const HANG_ALLOWLIST: &[&str] = &["carlson_rd", "carlson_rf", "carlson_rj", "qs_relation"];

/// Per-call wall-clock budget. Some Wolfram builtins (Monte Carlo
/// pricers, sparse solvers, plan generators) iterate to convergence
/// and would hang under a no-args invocation that produces a
/// degenerate input. Cap each call at 250ms via a sub-thread + join
/// timeout so a single bad builtin doesn't block the rest of the
/// walk. 250ms is generous for "should-return-error-immediately"
/// expected behaviour; anything longer is itself a finding.
const PER_CALL_TIMEOUT_MS: u64 = 250;

/// Hard cap on the whole-batch walk. 4600 builtins × 250ms worst case
/// = ~19 min; the empirical case is far faster (most error in <1ms),
/// so 5 min keeps CI bounded without prematurely killing legitimate
/// work. If this trips, narrow the names list (a recent merge added
/// a runaway).
const TOTAL_WALL_BUDGET_SECS: u64 = 300;

#[test]
fn wolfram_builtins_no_panic_on_empty_args() {
    use std::sync::mpsc;
    use std::time::{Duration, Instant};

    let names: Vec<String> = NAMES_FIXTURE
        .lines()
        .map(|s| s.trim().to_string())
        .filter(|s| {
            !s.is_empty()
                && !STRYKE_RESERVED.contains(&s.as_str())
                && !PANIC_ALLOWLIST.contains(&s.as_str())
                && !HANG_ALLOWLIST.contains(&s.as_str())
        })
        .collect();
    assert!(
        names.len() > 4000,
        "expected ~4500 wolfram builtins after allowlists (fixture {}, panic-allow {}, hang-allow {}), got {} — fixture out of sync?",
        NAMES_FIXTURE.lines().count(),
        PANIC_ALLOWLIST.len(),
        HANG_ALLOWLIST.len(),
        names.len()
    );

    let walk_started = Instant::now();
    let mut panicked: Vec<String> = Vec::new();
    let mut timed_out: Vec<String> = Vec::new();
    let mut completed: usize = 0;

    for name in &names {
        if walk_started.elapsed() > Duration::from_secs(TOTAL_WALL_BUDGET_SECS) {
            eprintln!(
                "WOLFRAM SMOKE: hit total wall budget after {} builtins; \
                 stopping early. Remaining {} not exercised.",
                completed,
                names.len() - completed
            );
            break;
        }

        let name_clone = name.clone();
        let (tx, rx) = mpsc::channel();

        // Spawn a worker thread for this one call. catch_unwind in the
        // worker captures Rust panics; the join-timeout above catches
        // wall-clock hangs. Acquire the global read lock INSIDE the
        // thread so each call has its own (the lock allows many
        // concurrent readers).
        let handle = std::thread::spawn(move || {
            // Suppress per-thread panic hook so failures don't dump
            // backtraces during the walk.
            let prev_hook = std::panic::take_hook();
            std::panic::set_hook(Box::new(|_| {}));

            let result = catch_unwind(AssertUnwindSafe(|| {
                let _guard = GLOBAL_FLAGS_LOCK.read();
                let code = format!("{}()", name_clone);
                let Ok(program) = stryke::parse(&code) else {
                    return; // parse fail = not a panic
                };
                let mut interp = VMHelper::new();
                let _ = interp.execute(&program); // runtime Err is fine
            }));

            std::panic::set_hook(prev_hook);
            let _ = tx.send(result.is_ok());
        });

        match rx.recv_timeout(Duration::from_millis(PER_CALL_TIMEOUT_MS)) {
            Ok(true) => {
                let _ = handle.join();
            }
            Ok(false) => {
                let _ = handle.join();
                panicked.push(name.clone());
            }
            Err(mpsc::RecvTimeoutError::Timeout) => {
                // Worker is still running. We don't join — that would
                // block. Thread leaks (it'll finish on its own when
                // the runaway builtin gives up or the test process
                // exits). 4600 max leaked threads is acceptable for a
                // test process.
                timed_out.push(name.clone());
            }
            Err(mpsc::RecvTimeoutError::Disconnected) => {
                let _ = handle.join();
                panicked.push(name.clone());
            }
        }
        completed += 1;
    }

    eprintln!(
        "WOLFRAM SMOKE: walked {} builtins in {:?} — {} panicked, {} timed out",
        completed,
        walk_started.elapsed(),
        panicked.len(),
        timed_out.len(),
    );

    if !panicked.is_empty() {
        eprintln!("Panics:");
        for n in &panicked {
            eprintln!("  panic: {}", n);
        }
    }
    if !timed_out.is_empty() {
        eprintln!("Timeouts (>{} ms):", PER_CALL_TIMEOUT_MS);
        for n in &timed_out {
            eprintln!("  hang:  {}", n);
        }
    }

    assert!(
        panicked.is_empty(),
        "{} wolfram builtin(s) PANIC on empty args (first 20: {:?})",
        panicked.len(),
        &panicked[..panicked.len().min(20)],
    );
    // Timeouts are surfaced (printed above) but don't fail the test —
    // some builtins legitimately need >250ms on a cold VM. If a
    // specific name is a known hang, add it to a skip list before
    // calling it a regression.
}
